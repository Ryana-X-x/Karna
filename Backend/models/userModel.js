const mongoose=require('mongoose')
const userSchema = new mongoose.Schema({
    fullName: {
        type: String,
        required: true
    },
    username: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true,
        minlength: 6
    },
    
}, { timestamps: true })

module.exports = mongoose.model('User', userSchema);